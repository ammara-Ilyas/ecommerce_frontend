import { Geist, Geist_Mono } from "next/font/google";
// import Header from "../components/layout/Header";
import "./globals.css";
import Footer from "@/components/layout/footer/Footer";
import NewsLatter from "@/components/layout/footer/widgets/NewLatter";
import FeatureCards from "@/components/layout/footer/widgets/FeaturedCard";
import NewsletterSection from "@/components/layout/footer/widgets/NewsLetter";
import ReduxProviders from "@/redux/ReduxProvider";
import { callPublicApi } from "@/libs/CallApis";
import Hero from "@/components/layout/Hero";
import Navbar from "@/components/layout/Navbar";
import { categories } from "@/components/widgets/home/Categories";
import { ToastContainer } from "react-toastify";
const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata = {
  title: "Shop you & Me",
  description: "Generated by create next app",
};

const fetchData = async () => {
  try {
    const productRes = await callPublicApi("/products", "GET");
    console.log("res in productRes list ", productRes);
    const CategoriesRes = await callPublicApi("/category", "GET");
    console.log("res in CategoriesRes list ", CategoriesRes);
    const bannerRes = await callPublicApi("/banners", "GET");
    console.log("res in bannerRes list ", bannerRes);
    const cartRes = await callPublicApi("/cart", "GET");
    console.log("res in bannerRes list ", cartRes);
    return { productRes, CategoriesRes, bannerRes, cartRes };
  } catch (error) {
    console.error("Error fetching data:", error);
    return {
      productRes: null,
      CategoriesRes: null,
      bannerRes: null,
      cartRes: null,
    };
  }
};
const { productRes, CategoriesRes, bannerRes, cartRes } = await fetchData();
export default function RootLayout({ children }) {
  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        <ReduxProviders
          product={productRes}
          categories={CategoriesRes}
          banners={bannerRes}
          cartsItems={cartRes}
        >
          <Navbar />
          <Hero />
          {/* <Header /> */}
          {children}
          <NewsLatter />
          <NewsletterSection />
          <FeatureCards />
          <Footer /> <ToastContainer position="top-right" autoClose={3000} />
        </ReduxProviders>
      </body>
    </html>
  );
}
